#include "../includes/libft.h"

int	ft_atoi(const char *str)
{
	int	answ;
	int	sign;

	sign = 1;
	answ = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n'
		|| *str == '\v' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '-')
		sign *= -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		answ = answ * 10 + (*str - '0');
		str++;
	}
	return (answ * sign);
}
