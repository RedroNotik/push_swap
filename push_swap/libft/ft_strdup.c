#include "../includes/libft.h"

char	*ft_strdup(char *s1)
{
	char	*s2;
	size_t	i;
	size_t	j;

	j = ft_strlen(s1);
	i = 0;
	s2 = (char *)malloc(j + 1);
	if (!s2)
		return (NULL);
	while (i < j)
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
